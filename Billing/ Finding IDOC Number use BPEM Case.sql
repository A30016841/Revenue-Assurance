SELECT 
EMMA.CASENR, 
EMMA.CREATED_DATE,
EMMA.EXT_UI AS "POD",
IDOC.MSGV1 AS "IDOC_NO.",
IDOC.MSGV3 AS "BILL_STRT_DATE"

 

FROM COMMERCIAL.EMMA_CASE EMMA

 

INNER JOIN "COMMERCIAL"."EMMA_CMSG_LINK" IDOC
ON EMMA.CASENR =IDOC.CASENR

 

WHERE EMMA.CCAT = 'MU64'

 

AND EMMA.CASENR IN ('0510820366')
